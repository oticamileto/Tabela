package com.tabela.beans;

public class TabelaMultifocal {
	
	private String nomeMult;
	private String nomeFornecedor;
	private String descricao;
	private Double valorMult;
	
	public String getNomeMult() {
		return nomeMult;
	}
	public void setNomeMult(String nomeMult) {
		this.nomeMult = nomeMult;
	}
	public String getNomeFornecedor() {
		return nomeFornecedor;
	}
	public void setNomeFornecedor(String nomeFornecedor) {
		this.nomeFornecedor = nomeFornecedor;
	}
	public Double getValorMult() {
		return valorMult;
	}
	public void setValorMult(Double valorMult) {
		this.valorMult = valorMult;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	

	
	
	
	
	
	
}
