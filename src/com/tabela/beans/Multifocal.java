package com.tabela.beans;

public class Multifocal {
	
	
	private int idMult;
	private String nomeMult;
	private String material;
	private Double indice;
	private String tratamento;
	private String descricao;
	private String adicao;
	private int altura;
	private int diametro;
	private String fotossensivel;
	private	Double valor;
	
	
	public int getIdMult() {
		return idMult;
	}
	public void setIdMult(int idMult) {
		this.idMult = idMult;
	}
	public String getNomeMult() {
		return nomeMult;
	}
	public void setNomeMult(String nomeMult) {
		this.nomeMult = nomeMult;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial(String material) {
		this.material = material;
	}
	public Double getIndice() {
		return indice;
	}
	public void setIndice(Double indice) {
		this.indice = indice;
	}
	public String getTratamento() {
		return tratamento;
	}
	public void setTratamento(String tratamento) {
		this.tratamento = tratamento;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getAdicao() {
		return adicao;
	}
	public void setAdicao(String adicao) {
		this.adicao = adicao;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}
	public int getDiametro() {
		return diametro;
	}
	public void setDiametro(int diametro) {
		this.diametro = diametro;
	}
	public String getFotossensivel() {
		return fotossensivel;
	}
	public void setFotossensivel(String fotossensivel) {
		this.fotossensivel = fotossensivel;
	}
	public Double getValor() {
		return valor;
	}
	public void setValor(Double valor) {
		this.valor = valor;
	}

	
	
	
}
